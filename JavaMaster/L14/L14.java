class L14 {
	public static void main(String args[]){
		int a; int b;
		a = Integer.parseInt(args[0]);
		b = Integer.parseInt(args[1]);
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("---------------");
		System.out.println("a == b:" + (a == b));
		System.out.println("a > b:" + (a > b));
		System.out.println("a >= b:" + (a >= b));
		System.out.println("a < b:" + (a < b));
		System.out.println("a <= b:" + (a <= b));
		System.out.println("a != b:" + (a != b));
	}
}