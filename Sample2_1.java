class Sample2_1 {
	public static void main(String[] args) {
		System.out.println("--- 整数 ---");
		System.out.println(255);
		System.out.println(0377);
		System.out.println(0xff);
		System.out.println(0b101);
		System.out.println("--- 実数 ---");
		System.out.println(12.33);
		System.out.println(3e4);
		System.out.println("--- 文字リテラル ---");
		System.out.println('A');
		System.out.println("--- Unicode ---");
		System.out.println('\u3012');
		System.out.println("--- 改行 ---");
		System.out.println('\n');
		System.out.println("--- 復帰 ---");
		System.out.println('\r');
		System.out.println("--- タブ文字 ---");
		System.out.println('\t');
		System.out.println("--- バックスペース ---");
		System.out.println('\b');
		System.out.println("--- シングルクォート ---");
		System.out.println('\'');
		System.out.println("--- ダブルクォート ---");
		System.out.println('\"');
		System.out.println("--- 円記号 ---");
		System.out.println('\\');
		System.out.println("--- フォームフィード ---");
		System.out.println('\f');
	}
}